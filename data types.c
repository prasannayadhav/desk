#include <stdio.h>
int main()
{
    int a=9;
    int b=-9;
    int c=55u;
    int d=99999L;
    printf("integer value  with positive data: %d\n",a);
    printf("integer value  with negative  data: %d\n",b);
    printf("integer value  with unsigned int data: %u\n",c);
    printf("integer value  with long long int: %ld",d);
    return 0;



}